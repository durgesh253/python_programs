# Negative indexes in Python are used to access elements or characters from the end of a sequence
# (e.g., a string, list, or tuple) rather than the beginning. 

my_list = [10, 20, 30, 40, 50]
last_element = my_list[-1] 
second_to_last = my_list[-2] 
print(last_element)
print(second_to_last) 
