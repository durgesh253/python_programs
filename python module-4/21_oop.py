# Class: A class is a blueprint or template for creating objects. It defines the structure and behavior of objects of that class.

# Object: An object is an instance of a class. It represents a real-world entity and encapsulates both data (attributes or properties) and behavior (methods or functions).

# Inheritance: Inheritance is a mechanism that allows a class (called a subclass or derived class) to inherit the properties and behaviors of another class (called a superclass or base class). It promotes code reusability.

# Encapsulation: Encapsulation is the bundling of data (attributes) and the methods (functions) that operate on that data into a single unit, known as a class. It hides the internal details of the class from the outside.

# Polymorphism: Polymorphism allows objects of different classes to be treated as objects of a common superclass. It enables flexibility by using a single interface to represent a general class of objects.

# Abstraction: Abstraction is the process of simplifying complex reality by modeling classes based on their essential characteristics. It involves showing only the necessary features of an object while hiding the unnecessary details.

