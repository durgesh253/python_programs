# What is Tuple ?
# A tuple in Python is a collection
#  data type that is similar to a list but with some key differences.

# diffrence between list and tuple
# 1) list 
# => list are mutable,which means you can change there content (add remove or modiy) after theyare created
#  Lists are created using square brackets [], and their elements are separated by commas

my_list = [1,2,3,4,5]
my_list[0] = 6   
print(my_list)

# 2) tuple
# =>  Tuples are immutable, which means once you create a tuple, you cannot change its content. 
# Tuples are created using parentheses (), and their elements are also separated by commas
my_tuple = (1,2,3,4)
print(my_tuple)